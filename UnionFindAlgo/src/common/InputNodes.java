public class InputNodes {

    public int nodeP;

    public int nodeQ;

    public int getNodeP() {
        return nodeP;
    }

    public void setNodeP(int nodeP) {
        this.nodeP = nodeP;
    }

    public int getNodeQ() {
        return nodeQ;
    }

    public void setNodeQ(int nodeQ) {
        this.nodeQ = nodeQ;
    }

    public InputNodes(int nodeP, int nodeQ) {
        this.nodeP = nodeP;
        this.nodeQ = nodeQ;
    }

}
